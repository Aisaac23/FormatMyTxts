    private void jButton9MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton9MouseReleased
        
        final int nFiles;

        File[] files = this.jFileChooser1.getSelectedFiles();
        
        nFiles = files.length;
        this.txtThreads = new txtFormatter[nFiles];
        int index = 0;
        
        this.counter.set(0);
        this.jProgressBar1.setValue(0);
        this.jProgressBar1.setMaximum(nFiles);
        
        for( File file : files)
        {
            this.txtThreads[index] = new txtFormatter(file);
            setChanges( this.txtThreads[index] );
            
            this.txtThreads[index].start();
            
            index++;
        }
        
    }//GEN-LAST:event_jButton9MouseReleased

