    private static void recursivelyDisableComponents(JComponent comp, boolean disEn)
    {
        synchronized(comp.getTreeLock()) {
            if(comp.getComponentCount() > 1)
                for (Component c : comp.getComponents())
                {
                    c.setEnabled(disEn);
                    recursivelyDisableComponents((JComponent)c, disEn);
                }
        }
    }
    
    private void setChanges(txtFormatter currentOne)
    {
        currentOne.setProgress(this.jProgressBar1);
        currentOne.setCounter(counter);
        
        if(this.jCheckBox1.isSelected())
        {
                currentOne.setWillDelete(true);
                currentOne.setDeleteBefore( this.jRadioButton4.isSelected() );
                currentOne.setDelete( this.jTextField4.getText() );
        }
        
        if(this.jCheckBox2.isSelected())
        {
            currentOne.setWillNormalize( true );
            
            if( this.jRadioButton1.isSelected() )
                currentOne.setNormalize(" ");
            else if( this.jRadioButton2.isSelected() )
                currentOne.setNormalize("\t");
            else if( this.jRadioButton3.isSelected() )
                currentOne.setNormalize("\n");
            else if( this.jRadioButton8.isSelected() )
                currentOne.setNormalize("ANSI");
        }
        
        if(this.jCheckBox3.isSelected())
        {
                currentOne.setWillGroup(true);
                currentOne.setGroup( (Integer)this.jSpinner1.getValue() );
                currentOne.setSeparator( this.jTextField1.getText() );
        }
        
        if(this.jCheckBox4.isSelected())
        {
                currentOne.setWillReplace(true);
                currentOne.setReplace( this.jTextField2.getText() );
                currentOne.setWith( this.jTextField3.getText() );
        }
        
        if(this.jCheckBox5.isSelected())
        {
                currentOne.setWillCase(true);
                
                int caseMode = -1;
                
                caseMode = this.jRadioButton6.isSelected() ? 0 : caseMode;
                caseMode = this.jRadioButton7.isSelected() ? 1 : caseMode;
                caseMode = this.jRadioButton9.isSelected() ? 2 : caseMode;
                
                currentOne.setCaseMode( caseMode );
                currentOne.setCaseMode( caseMode );
                currentOne.setCaseMode( caseMode );
                
        }
        
        if(this.jCheckBox7.isSelected())
        {
            currentOne.setWillSaveAs(true);
            currentOne.setSaveAs( this.jTextField5.getText() );
        }
       
    }
    
